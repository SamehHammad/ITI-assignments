let textPar = document.getElementById("PAR").style;
function ChangeFont(font) {
  textPar.fontFamily = font;
}
function ChangeAlign(align) {
  textPar.textAlign = align;
}
function ChangeHeight(height) {
  textPar.lineHeight = height;
}
function ChangeLSpace(space) {
  textPar.letterSpacing = space;
}
function ChangeIndent(indent) {
  textPar.textIndent = indent;
}
function ChangeTransform(transform) {
  textPar.textTransform = transform;
}
function ChangeDecorate(decorate) {
  textPar.textDecoration = decorate;
}
function ChangeBorder(border) {
  textPar.border = border;
}

function ChangeBorderColor(color) {
  textPar.borderColor = color;
}